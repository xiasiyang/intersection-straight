
%*************************************************************
%AC0方程,输出z就是AC0包的平均数
function z=fun(t,x,flag,c_0,u_0)
%   u_0=[0,0];%服务率
%   c_0=[0,0];%变差系数
  lambda0=4;%AC0的到达率，5pks/s
  %****************************
  g=(x)^2+2*(x)*(c_0)^2+1;
  g1=-(x+1-sqrt(g))/(1-(c_0)^2);
  z=g1*u_0+lambda0;
end